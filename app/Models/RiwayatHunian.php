<?php

namespace App\Models;

use Illuminate\Database\Eloquent\Model;

class RiwayatHunian extends Model
{
    /*
    |--------------------------------------------------------------------------
    | TABLE
    |--------------------------------------------------------------------------
    */

    protected $table =
        'riwayat_hunians';

    /*
    |--------------------------------------------------------------------------
    | PRIMARY KEY
    |--------------------------------------------------------------------------
    */

    protected $primaryKey =
        'id_riwayat_hunian';

    /*
    |--------------------------------------------------------------------------
    | MASS ASSIGNMENT
    |--------------------------------------------------------------------------
    */

    protected $fillable = [

        'id_user',

        'id_kost',

        'id_kamar',

        'tanggal_masuk',

        'tanggal_keluar',

        'status'

    ];

    /*
    |--------------------------------------------------------------------------
    | CASTS
    |--------------------------------------------------------------------------
    */

    protected $casts = [

        'tanggal_masuk' => 'date',

        'tanggal_keluar' => 'date',

    ];

    /*
    |--------------------------------------------------------------------------
    | USER
    |--------------------------------------------------------------------------
    */

    public function user()
    {
        return $this->belongsTo(

            User::class,

            'id_user',

            'id'

        );
    }

    /*
    |--------------------------------------------------------------------------
    | KOST
    |--------------------------------------------------------------------------
    */

    public function kost()
    {
        return $this->belongsTo(

            Kost::class,

            'id_kost',

            'id'

        );
    }

    /*
    |--------------------------------------------------------------------------
    | KAMAR
    |--------------------------------------------------------------------------
    */

    public function kamar()
    {
        return $this->belongsTo(

            KamarKost::class,

            'id_kamar',

            'id_kamar'

        );
    }
}